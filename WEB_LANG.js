// Sprachumschaltung
function setLanguage(lang) {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (translations[lang][key]) {
      el.textContent = translations[lang][key];
    }
  });
  // Platzhalter für Inputs
  document.querySelectorAll('[data-i18n-ph]').forEach(el => {
    const key = el.getAttribute('data-i18n-ph');
    if (translations[lang][key]) {
      el.setAttribute('placeholder', translations[lang][key]);
    }
  });
  // Optionale: HTML lang-Attribut setzen
  document.documentElement.lang = (lang === 'ti') ? 'ti' : 'de';
}

// Sprachumschalter Dropdown
document.addEventListener('DOMContentLoaded', function() {
  const langSelect = document.getElementById('audio-lang');
  if (langSelect) {
    langSelect.addEventListener('change', function() {
      setLanguage(this.value === 'tigrinya' ? 'ti' : 'de');
    });
    // Standard: Deutsch
    setLanguage(langSelect.value === 'tigrinya' ? 'ti' : 'de');
  }
});